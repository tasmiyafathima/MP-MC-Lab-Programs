.MODEL SMALL
DISP MACRO MSG
LEA DX,MSG
MOV AH,09H
INT 21H
ENDM

.DATA
ROW DB 02 DUP(0)
COL DB 02 DUP(0)
MSG1 DB 0DH,0AH,"ENTER X-COORDINATE::$"
MSG2 DB 0DH,0AH,"ENTER Y-COORDINATE::$"

.CODE
MOV AX,@DATA
MOV DS,AX
DISP MSG1
MOV SI,OFFSET ROW
CALL READ
DISP MSG2
MOV SI,OFFSET COL
CALL READ
MOV SI,OFFSET ROW
MOV AH,[SI]
INC SI
MOV AL,[SI]
SUB AX,3030H
AAD
MOV DH,AL
MOV SI,OFFSET COL
MOV AH,[SI]
INC SI
MOV AL,[SI]
SUB AX,3030H
AAD
MOV DL,AL
MOV AH,00
MOV AL,03H
INT 10H
MOV AH,02H
INT 10H
JMP FINAL
READ PROC NEAR
MOV CX,02H
BACK:MOV AH,01H
     INT 21H
     MOV [SI],AL
     INC SI
     DEC CX
     JNZ BACK
     RET
    READ ENDP
FINAL: MOV AH,01H
       INT 21H
   MOV AH,4CH
   INT 21H
   END
